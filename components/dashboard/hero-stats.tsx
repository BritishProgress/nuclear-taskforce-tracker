'use client';

import { cn } from '@/lib/utils';
import { StatusCounts, OverallStatus } from '@/lib/types';
import { Disclaimer } from '@/components/shared';
import { 
  AlertCircle, 
  CheckCircle,
  Atom
} from 'lucide-react';

interface HeroStatsProps {
  counts: StatusCounts;
  className?: string;
  onStatusClick?: (status: OverallStatus | 'all') => void;
}

export function HeroStats({ counts, className, onStatusClick }: HeroStatsProps) {
  const mainStats = [
    {
      label: 'Completed',
      value: counts.completed,
      icon: CheckCircle,
      color: 'text-dark-green',
      bgColor: 'bg-neon-green/20',
      status: 'completed' as OverallStatus,
    },
    {
      label: 'On Track',
      value: counts.on_track,
      icon: Atom,
      color: 'text-dark-green',
      bgColor: 'bg-dark-green/20',
      status: 'on_track' as OverallStatus,
    },
    {
      label: 'Off Track',
      value: counts.off_track,
      icon: AlertCircle,
      color: 'text-deep-red',
      bgColor: 'bg-deep-red/10',
      status: 'off_track' as OverallStatus,
    },
  ];

  const totalStat = {
    label: 'Total',
    value: counts.total,
    icon: 'atom' as const,
    color: 'text-dark-green',
    bgColor: 'bg-dark-green/10',
    status: 'all' as const,
  };

  return (
    <div className={cn('', className)}>
      {/* Hero Header */}
      <div className="text-center mb-8">
        <div className="mb-3 md:mb-6">
          <img 
            src="/logo.svg" 
            alt="Nuclear Taskforce Tracker" 
            className="h-6 md:h-10 mx-auto"
          />
        </div>
        <h1 className="font-display text-4xl md:text-5xl lg:text-6xl font-bold text-dark-green mb-4 tracking-tight">
          Nuclear Taskforce Tracker
        </h1>
        <p className="text-lg md:text-xl text-muted-foreground max-w-2xl mx-auto">
          Tracking the progress of government departments, regulators and industry in implementing the UK Nuclear Regulatory Taskforce's recommendations
        </p>
      </div>

      {/* Progress Bar & Stats */}
      <div className="flex flex-col items-center gap-4 sm:gap-8">
        {/* Horizontal Progress Bar */}
        <div className="w-full max-w-2xl">
          <div className="relative h-8 sm:h-10 bg-muted rounded-md overflow-hidden">
            {/* Completed segment */}
            {counts.completed > 0 && (
              <div
                className="absolute left-0 top-0 h-full bg-neon-green transition-all duration-500 ease-out"
                style={{ width: `${(counts.completed / counts.total) * 100}%` }}
              />
            )}
            {/* On Track segment */}
            {counts.on_track > 0 && (
              <div
                className="absolute top-0 h-full bg-dark-green/30 transition-all duration-500 ease-out"
                style={{
                  left: `${(counts.completed / counts.total) * 100}%`,
                  width: `${(counts.on_track / counts.total) * 100}%`,
                }}
              />
            )}
            {/* Off Track segment */}
            {counts.off_track > 0 && (
              <div
                className="absolute top-0 h-full bg-deep-red/30 transition-all duration-500 ease-out"
                style={{
                  left: `${((counts.completed + counts.on_track) / counts.total) * 100}%`,
                  width: `${(counts.off_track / counts.total) * 100}%`,
                }}
              />
            )}
            {/* Atom icon in center */}
            <div className="absolute inset-0 flex items-center justify-center">
              <Atom className="h-5 w-5 sm:h-6 sm:w-6 text-dark-green" />
            </div>
          </div>
        </div>

        {/* Stats Grid - Centered */}
        <div className="flex justify-center w-full">
          <div className="flex flex-col w-full sm:w-auto sm:grid sm:grid-cols-3 gap-2 sm:gap-4">
            {/* Main three stats */}
            {mainStats.map((stat) => {
              const Icon = stat.icon;
              const isClickable = !!onStatusClick;
              const Component = isClickable ? 'button' : 'div';
              const percentage = counts.total === 0 ? 0 : Math.round((stat.value / counts.total) * 100);
              return (
                <Component
                  key={stat.label}
                  onClick={isClickable ? () => onStatusClick?.(stat.status) : undefined}
                  className={cn(
                    'flex flex-row sm:flex-col items-center justify-center gap-3 sm:gap-1 py-3 px-4 sm:px-8 sm:py-4 rounded-xl transition-all w-full min-w-[120px]',
                    stat.bgColor,
                    isClickable && 'cursor-pointer hover:scale-105 hover:shadow-md active:scale-95'
                  )}
                >
                  <div className="flex items-center gap-2 sm:flex-col sm:gap-1">
                    {Icon && (
                      <Icon className={cn(
                        'h-5 w-5 sm:h-6 sm:w-6',
                        stat.color,
                        stat.status === 'completed' && 'font-bold'
                      )} />
                    )}
                    <div className="flex items-center gap-1.5 sm:flex-col sm:items-center sm:gap-0">
                      <span className={cn(
                        'text-2xl sm:text-4xl font-bold font-mono',
                        stat.color,
                        stat.status === 'completed' && 'font-extrabold'
                      )}>
                        {stat.value}
                      </span>
                      <span className="text-xs sm:text-sm font-mono text-muted-foreground">
                        <span className="sm:hidden">({percentage}%)</span>
                        <span className="hidden sm:inline">{percentage}%</span>
                      </span>
                    </div>
                  </div>
                  <span className={cn(
                    'text-sm sm:text-sm sm:mt-1',
                    stat.status === 'completed' ? 'font-semibold text-dark-green' : 'text-muted-foreground'
                  )}>
                    {stat.label}
                  </span>
                </Component>
              );
            })}

            {/* Total stat - underneath on desktop, spans all 3 columns */}
            {(() => {
              const isClickable = !!onStatusClick;
              const Component = isClickable ? 'button' : 'div';
              return (
                <Component
                  onClick={isClickable ? () => onStatusClick?.(totalStat.status) : undefined}
                  className={cn(
                    'flex flex-row items-center justify-center gap-3 p-2 sm:p-3 rounded-xl transition-all w-full sm:col-span-3',
                    totalStat.bgColor,
                    isClickable && 'cursor-pointer hover:scale-105 hover:shadow-md active:scale-95'
                  )}
                >
                  <span className={cn('text-xl sm:text-2xl font-bold font-mono', totalStat.color)}>
                    {totalStat.value}
                  </span>
                  <span className="text-sm sm:text-sm text-muted-foreground">
                    {totalStat.label}
                  </span>
                </Component>
              );
            })()}
          </div>
        </div>

        {/* Disclaimer */}
        <Disclaimer />
      </div>
    </div>
  );
}

